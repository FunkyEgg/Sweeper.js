const fs = require('fs');

/**
 *  Initializes the html and css file
 *  @param {String} title The title of the website
 */
function init(title) {
    if (!title) { 
        console.error('Sweeper.js: No title given, defaulting to "Sweepjs"');
        title = 'Sweeperjs';
    }

    const htmlInitText = `<!-- Generated by Sweeper.js -->
<!DOCTYPE html>
<html lang="en">
<head>
<link rel="stylesheet" href="css/style.css">
<meta charset="UTF-8">
<meta http-equiv="X-UA-Compatible" content="IE=edge">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>${title}</title>
</head>
<body>\n`;

    if (!fs.existsSync('./sweeperjs')) {
        fs.mkdirSync('./sweeperjs');
    }

    fs.writeFileSync('./sweeperjs/index.html', htmlInitText);
    fs.writeFileSync('./sweeperjs/style.css', '/* Generated by Sweeper.js */');
}

module.exports = {
    init
}